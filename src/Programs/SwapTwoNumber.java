package Programs;

public class SwapTwoNumber {

	public static void main(String[] args) {
		
		int a=10;
		
		int b=5;
		
		int temp;
		
		System.out.println("After Swaping a is  :"+a);
		System.out.println("After Swaping b is  :"+b);
		
		temp=a;
		a=b;
		b=temp;
		
		System.out.println("After Swaping a is  :"+a);
		System.out.println("After Swaping b is  :"+b);

	}

}
